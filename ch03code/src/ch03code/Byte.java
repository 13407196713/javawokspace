package ch03code;

public class Byte {
	public static void main(String[] args) {      
		byte x = 22;                                      
		byte y = 022;                                    
		byte z = 0x22;                                   
		System.out.println("转化成十进制,x=" + x);
		System.out.println("转化成十进制,y=" + y);
		System.out.println("转化成十进制,z=" + z);
		}

}
