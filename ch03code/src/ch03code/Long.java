package ch03code;

public class Long {
	public static void main(String[] args)   
	{
	long  x=22L;                                 
	long  y=022L;                                
	long  z=0x22L;                                
	System.out.println("转化成十进制,x="+ x);
	System.out.println("转化成十进制,y="+ y);
	System.out.println("转化成十进制,z="+ z);
	}

}
